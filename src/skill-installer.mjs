import { cp, mkdir, writeFile } from 'node:fs/promises'
import { join, resolve } from 'node:path'
import { homedir } from 'node:os'

export function defaultCodexHome() {
  return process.env.CODEX_HOME || join(homedir(), '.codex')
}

export function parseInstallArgs(argv) {
  const options = { codexHome: defaultCodexHome() }

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index]

    if (arg === '--codex-home') {
      index += 1
      if (!argv[index]) throw new Error('--codex-home requires a path')
      options.codexHome = argv[index]
      continue
    }

    if (arg === '--help' || arg === '-h') {
      options.help = true
      continue
    }

    throw new Error(`Unknown argument: ${arg}`)
  }

  return options
}

export function installUsage(entry = 'cine-make install-skill') {
  return [
    'Usage:',
    `  ${entry} [--codex-home <path>]`,
    '',
    'Examples:',
    '  npx cine-make install-skill',
    '  npm i -g cine-make',
    '  cine-make install-skill',
    '',
    'Installs skills/cine-make into <codex-home>/skills/cine-make and records this compiler path.'
  ].join('\n')
}

export async function installCodexSkill({ codexHome = defaultCodexHome(), compilerRoot }) {
  if (!compilerRoot) throw new Error('installCodexSkill requires compilerRoot')

  const resolvedCompilerRoot = resolve(compilerRoot)
  const sourceSkill = join(resolvedCompilerRoot, 'skills', 'cine-make')
  const targetSkill = join(resolve(codexHome), 'skills', 'cine-make')
  const referencesDir = join(targetSkill, 'references')

  await mkdir(join(resolve(codexHome), 'skills'), { recursive: true })
  await cp(sourceSkill, targetSkill, { recursive: true, force: true })
  await mkdir(referencesDir, { recursive: true })
  await writeFile(
    join(referencesDir, 'compiler-location.md'),
    [
      '# Cine Make Compiler Location',
      '',
      'This file is generated by Cine Make install-skill.',
      '',
      'Compiler root:',
      '',
      '```text',
      resolvedCompilerRoot,
      '```',
      '',
      'The compiler root is the directory containing `src/cli.mjs`.'
    ].join('\n') + '\n',
    'utf8'
  )

  return { targetSkill, compilerRoot: resolvedCompilerRoot }
}

